# Declaración de variables
numero = 10
Numero = 20
saludo = "¡Hola mundo!"

# Mostramos el contenido de las variables
print(numero)
print(Numero)           # Diferencia entre minúsculas y mayúsculas
print(saludo)

print(numero + Numero)
print(numero - 25)
print("Saludo: " + saludo)

print(35)
print("Saludos")
print("")

# Mostramos el tipo de las variables
print(type(numero))
print(type(saludo))

# Mostrar el tipo de diferentes valores utilizando la función TYPE
print(type(3))
print(type(3.4))
print(type("3"))
print(type("tres"))
print(type(3==3))
print(type(('1','2','3')))
print(type(["1","2","3"]))
print(type({"1","2","3"}))
print(type([1,2,3]))